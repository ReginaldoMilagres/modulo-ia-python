# Esse programa calcula o volume de uma caixa retangular

# Solicita as dimensões da caixa
comprimento = int(input("Digite o comprimento: "))
largura = int(input("Digite o largura: "))
altura = int(input("Digite o altura: "))

# Calcula o volume da caixa
volume = comprimento * largura * altura

# Exibe o resultado
print (f"O volume da caixa m³ {volume}") 